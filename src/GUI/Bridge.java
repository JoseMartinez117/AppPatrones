    private void lblGiveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblGiveMouseClicked
        if(!item.isBlank()){      
            if(txtDurability.getText().isEmpty() && txtDamage.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Tu item tiene que tener durabilidad y daño");
            }else{
                setImageLabel(lblItem1,"src/Assets/IMG/Bridge/"+item+"_Img.png");
                lblDamage.setText(txtDamage.getText());
                lblDurability.setText(txtDurability.getText());
                this.damage= Integer.parseInt(txtDamage.getText());
                this.durability = Integer.parseInt(txtDurability.getText());
                this.fdura = Integer.parseInt(txtDurability.getText());
            }
            
        }else{
           JOptionPane.showMessageDialog(null, "No seleccionaste un item"); 
        }

        
    }//GEN-LAST:event_lblGiveMouseClicked

    private void lblUseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblUseMouseClicked
        if(!item.isBlank()){
            if(durability>0){
                switch (item){
                    case "sword":
                        lblText.setText("<html>"+lblLink.getText()+" arremete con su espada para atacar a los enemigos<html>");
                        this.durability=this.durability-10;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                    case "shield":
                        lblText.setText("<html>"+lblLink.getText()+" usa con astucia su escudo para defenderse de los enemigos<html>");
                        this.durability=this.durability-10;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                    case "staff":
                        lblText.setText("<html>"+lblLink.getText()+" conjura las magias de las arenas con su baculo para atacar a los enemigos<html>");
                        this.durability=this.durability-20;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                }
            }else{
                JOptionPane.showMessageDialog(null, "La durabilidad esta en 0, no lo puedes usar mas!!, debes repararlo"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, lblLink.getText()+" no tiene un item"); 
        }
    }//GEN-LAST:event_lblUseMouseClicked

    private void lblRepairMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblRepairMouseClicked
        if(!item.isBlank()){
            if(this.durability<(fdura+1)){

                switch (item){
                    case "sword":
                        lblText.setText("<html>"+lblLink.getText()+" utiliza sus conocimienntos para reparar su espada<html>");
                        this.durability=this.durability+5;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                    case "shield":
                        lblText.setText("<html>"+lblLink.getText()+" pule su escudo y lo repara para seguir luchando<html>");
                        this.durability=this.durability+5;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                    case "staff":
                        lblText.setText("<html>"+lblLink.getText()+" golpea su palo contra un arbol para repararlo<html>");
                        this.durability=this.durability+5;
                        if(this.durability<0){
                            this.durability=0;  
                        }
                        lblDurability.setText(String.valueOf(this.durability));
                    break;

                }
            }else{
                JOptionPane.showMessageDialog(null, "Tu item no puede repararse mas!!"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, lblLink.getText()+" no tiene un item"); 
        }
        
    }//GEN-LAST:event_lblRepairMouseClicked

    private void lblBuffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuffMouseClicked
        if(!item.isBlank()){
            if(lblLink.getText().equals("dekuLink")){

                lblText.setText("Deku-Link potencia sus items y los lleva a otro nivel");
                this.durability=this.durability+999;
                this.damage=this.damage+999;

                lblDurability.setText(String.valueOf(this.durability));
                lblDamage.setText(String.valueOf(this.damage));

            }else{
                JOptionPane.showMessageDialog(null, "No estas en modo Deku-Link"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, lblLink.getText()+" no tiene un item");
        }
        
    }//GEN-LAST:event_lblBuffMouseClicked

