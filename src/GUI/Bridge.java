    private void txtDamageKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDamageKeyTyped
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;

        if (!numeros)
        {
            evt.consume();
        }

        if (txtDamage.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDamageKeyTyped

