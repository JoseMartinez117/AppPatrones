    private void txtPokemonKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPokemonKeyTyped
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;

        if (!numeros)
        {
            evt.consume();
        }

        if (txtPokemon.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_txtPokemonKeyTyped

