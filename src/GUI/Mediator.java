    private void lblPokemonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblPokemonMouseClicked
                
        String[] opciones = {"Curar", "Intercambiar"};
        
        
        int seleccion = JOptionPane.showOptionDialog(
            null, 
            "Elige una opción:", 
            "Opciones de acción", 
            JOptionPane.DEFAULT_OPTION, // 
            JOptionPane.INFORMATION_MESSAGE, 
            null, 
            opciones, 
            opciones[0] 
        );
        
        // Evaluamos la selección del usuario
        if (seleccion == 0) {
            lblText.setText("<html>Entregas tus pokemons a la enfermera Joy para que los cure, tus pokemons son curados y entregados a ti de nuevo<html>");
        } else if (seleccion == 1) {
            lblText.setText("<html>Entregas tu Pikachu a la enfermera Joy y esta te entrega su Jolteon<html>");
        } else {
            JOptionPane.showMessageDialog(null, "No has seleccionado ninguna opción");
        }
        
        
        
    }//GEN-LAST:event_lblPokemonMouseClicked

