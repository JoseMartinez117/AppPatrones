    private void setTxt(String type){
        switch(this.value){
            case "A":
                
                if(type.equals("elemental")){
                    lblText.setText("<html>El viajero convoca los viento usando sus manos para crear un remolino<html>");
                }else if(type.equals("ultimate")){
                    lblText.setText("<html>El viajero convoca los viento usando sus espada para crear un tornado<html>");
                }
                
                break;
           
            case "D":
                if(type.equals("elemental")){
                    lblText.setText("<html>El viajero utiliza su espada invcando el poder de las flores para atacar<html>");
                }else if(type.equals("ultimate")){
                    lblText.setText("<html>El viajero utiliza el poder de las flores para generar un campo de explosiones<html>");
                }
                break;
                
            case "E":
                if(type.equals("elemental")){
                    lblText.setText("<html>El viajero lanza 3 rayos desde su espada<html>");
                }else if(type.equals("ultimate")){
                    lblText.setText("<html>El viajero convoca con sus manos un rayo<html>");
                }
                break;
                
            case "G":
                if(type.equals("elemental")){
                    lblText.setText("<html>El viajero convoca una piedra para protegerse<html>");
                }else if(type.equals("ultimate")){
                    lblText.setText("<html>El viajero provoca con sus pu√±os un fuerte temblor<html>");
                }
                break;
                
            case "H":
                if(type.equals("elemental")){
                    lblText.setText("<html>El viajero apunta con sus manos y dispara gotas de agua envenendada<html>");
                }else if(type.equals("ultimate")){
                    lblText.setText("<html>El viajero convoca una esfera de agua que absorbe a los enemigos<html>");
                }
                
                break;
        
        }
    
    }
    
